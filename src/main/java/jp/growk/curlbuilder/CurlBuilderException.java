package jp.growk.curlbuilder;

public class CurlBuilderException extends RuntimeException {

	/**
	 * 
	 */
	private static final long serialVersionUID = -5677653603486463527L;

	public CurlBuilderException() {
		super();
	}

	public CurlBuilderException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public CurlBuilderException(String message, Throwable cause) {
		super(message, cause);
	}

	public CurlBuilderException(String message) {
		super(message);
	}

	public CurlBuilderException(Throwable cause) {
		super(cause);
	}
	
	

}
